// Function to fill in placeholders in a template string.
// It replaces each '{}' in the template sequentially with the provided values.
function fillTemplate(template:any, ...values:any) {
  let i = 0;
  return template.replace(/{}/g, () => values[i++]);
}

// Generate 500 human-like queries
const humanQueries = [];
const totalQueries = 500;

for (let i = 0; i < totalQueries; i++) {
  const template = getRandomElement(templates);
  const adj1 = getRandomElement(adjectives);
  const item = getRandomElement(items);
  const context = getRandomElement(contexts);
  const adj2 = getRandomElement(adjectives);

  // Count placeholders in the template to determine if we need three or four values
  const placeholderCount = (template.match(/{}/g) || []).length;

  let query = "";
  if (placeholderCount === 3) {
    query = fillTemplate(template, adj1, item, context);
  } else if (placeholderCount === 4) {
    query = fillTemplate(template, adj1, item, context, adj2);
  } else {
    // Fallback if unexpected, defaulting to three placeholders
    query = fillTemplate(template, adj1, item, context);
  }
  
  humanQueries.push(query);
}

